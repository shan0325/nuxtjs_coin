<script>
export default {
	mounted() {
		setInterval(() => {
			this.timePlay('hMinutePlay');
		}, 36000000);

		setInterval(() => {
			this.timePlay('hSecondPlay');
		}, 3600000);

		setInterval(() => {
			this.timePlay('mMinutePlay');
		}, 600000);

		setInterval(() => {
			this.timePlay('mSecondPlay');
		}, 60000);

		setInterval(() => {
			this.timePlay('minutePlay');
		}, 10000);

		setInterval(() => {
			this.timePlay('secondPlay');
		}, 1000);
	},
	methods: {
		timePlay(className) {
			document.querySelector('.codepen').classList.remove('play');
			let aa = document.querySelector(`ul.${className} li.active`);

			if (aa === null) {
				aa = document.querySelector(`ul.${className}`).childNodes[0];
				aa.classList.add('before');
				aa.classList.remove('active');
				aa.nextElementSibling.classList.add('active');
				document.querySelector('.codepen').classList.add('play');
			} else if (
				document
					.querySelector(`ul.${className}`)
					.lastChild.classList.contains('active')
			) {
				document
					.querySelector(`ul.${className}`)
					.childNodes.forEach(el => {
						if (el.tagName === 'LI') {
							el.classList.remove('before');
						}
					});

				aa.classList.add('before');
				aa.classList.remove('active');
				aa = document.querySelector(`ul.${className}`).childNodes[0];
				aa.classList.add('active');
				document.querySelector('.codepen').classList.add('play');
			} else {
				document
					.querySelector(`ul.${className}`)
					.childNodes.forEach(el => {
						if (el.tagName === 'LI') {
							el.classList.remove('before');
						}
					});

				aa.classList.add('before');
				aa.classList.remove('active');
				aa.nextElementSibling.classList.add('active');
				document.querySelector('.codepen').classList.add('play');
			}
		},
	},
};
</script>

